# Calculo da área

# A fórmula para calcular a área de um espaço retangular ou quadrado é multiplicar a largura pelo comprimento

# Dica a multiplicação é usado *
# pedir para o usuario a largura e o comprimento

largura = float(input("Digite a largura: "))
comprimento = float(input("Digite o comprimento: "))

calculo = largura * comprimento

print('Resultado ' + str(calculo))

print(f'Resultado {calculo}')