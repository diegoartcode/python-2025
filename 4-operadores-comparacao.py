# 4-operadores-comparacao.py
num1 = 35
num2 = 3

igualdade = num1 == num2
print('igualdade')
print(igualdade)

diferenca = num1 != num2
print('diferenca')
print(diferenca)

maior_que = num1 > num2
print('maior que')
print(maior_que)

menor_que = num1 < num2
print('menor que')
print(menor_que)

maior_igual = num1 >= num2
print('maior ou igual')
print(maior_igual)

menor_igual = num1 <= num2
print('menor ou igual')
print(menor_igual)